    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        if (selectRowTable < 0) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn sản phẩm để cập nhật !", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            String maSP = tableSanPham.getValueAt(selectRowTable, 1).toString();

            // lay rs tu quan ly san pham
            QLSP = new QuanLySanPham();
            rs = QLSP.getDataSPId(tableName, maSP);
            try {
                rs.next(); //goi toi rs.next()
            } catch (SQLException ex) {
                Logger.getLogger(FrameQuanLy.class.getName()).log(Level.SEVERE, null, ex);
            }

            // goi toi cap nhat san pham
            CNSP = new FrameCapNhatSanPham(nv, rs, selectIndexComboBox, "UPDATE", this);
//            this.dispose();
            CNSP.setVisible(true);
        }
    }//GEN-LAST:event_btCapNhatActionPerformed


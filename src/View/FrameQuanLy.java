    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        if (selectRowTable < 0) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn 1 sản phẩm để xoá !", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            String maSP = tableSanPham.getValueAt(selectRowTable, 1).toString();
            int option = JOptionPane.showConfirmDialog(null, "Bạn muốn xoá sản phẩm " + maSP + " !", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                QLSP = new QuanLySanPham();
                QLSP.XoaId(tableName, maSP);
                comboBoxLoaiSP.setSelectedIndex(selectIndexComboBox);
            } else {
            }
        }
    }//GEN-LAST:event_btXoaActionPerformed


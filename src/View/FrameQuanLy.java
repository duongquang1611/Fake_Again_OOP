    public void tao_arrayData(ResultSet rs) {
//        System.out.println("Tao array");
        try {
            // de co the lay ten cot trong bang va so cot trong bang
            ResultSetMetaData rsMD = rs.getMetaData();

            // lay so cot
            colNumber = rsMD.getColumnCount();

            //tao mang moi co colNumber chieu, tinh ca cot STT
            arrayData = new String[colNumber + 1];

        } catch (SQLException ex) {
            Logger.getLogger(FrameNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Override
    public void load_ds_SP(String tableName) {
        model = (DefaultTableModel) tableSanPham.getModel();
        model.setRowCount(0);
        con.connect();
        int STT = 0;
        //lay data
        rs = con.getData(tableName);
        System.out.println("Load Data Table");

        // tao mang 
        tao_arrayData(rs);

        try {
            while (rs.next()) {
//                System.out.println(rs.getString(2));
                STT++;
                arrayData[0] = String.valueOf(STT);
                for (int i = 0; i < colNumber; i++) {
                    arrayData[i + 1] = rs.getString(i + 1);
                }
                model.addRow(arrayData);

                //test debug
//                System.out.println(arrayData[3]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


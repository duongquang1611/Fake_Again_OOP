    @Override
    public void showSoLuong() {
        setEditableText(false);

        QLSP = new QuanLySanPham();
        int SL_Sach = QLSP.getSoLuongSP("sach");
        int SL_DiaNhac = QLSP.getSoLuongSP("dianhac");
        int SL_DiaPhim = QLSP.getSoLuongSP("diaphim");
        
        double GT_Sach = QLSP.getGiaTriSP("sach");
        double GT_DiaNhac = QLSP.getGiaTriSP("dianhac");
        double GT_DiaPhim = QLSP.getGiaTriSP("diaphim");
        
        double tongGT = GT_Sach + GT_DiaNhac + GT_DiaPhim;
        int tongSL = SL_Sach + SL_DiaNhac + SL_DiaPhim;
 
        tfSL_Sach.setText(String.valueOf(SL_Sach));
        tfSL_DiaNhac.setText(String.valueOf(SL_DiaNhac));
        tfSL_DiaPhim.setText(String.valueOf(SL_DiaPhim));
        tfSL.setText(String.valueOf(tongSL));
        
        tfGT.setText(QLSP.convertDoubleToStringWithFormat(tongGT));
    }

    public void setEditableText(boolean check) {
        tfSL_Sach.setEditable(check);
        tfSL_DiaNhac.setEditable(check);
        tfSL_DiaPhim.setEditable(check);
        tfSL.setEditable(check);
        tfGT.setEditable(check);
    }
